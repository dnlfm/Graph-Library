    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        grafo = new Grafo();
        nome_arquivo_lido = txtArquivoEntrada.getText();
        grafo.lerArquivo(nome_arquivo_lido);
        if(grafo.getArquivoFoiLido()){
            JOptionPane.showMessageDialog(null, "Arquivo lido com sucesso!");
            habilitarOperacoes();
            btnEscreverEmArquivo.setEnabled(true);
            if(txtArquivoSaida.getText().isEmpty()){
                File directory = new File(OUTPUT_PATH);
                if(!directory.exists()){
                    directory.mkdir();
                }
                String path = OUTPUT_PATH + FILE_SEPARATOR + "out_" + jFileChooser1.getSelectedFile().getName();
                txtArquivoSaida.setText(path);
            }
            
        } else{
            JOptionPane.showMessageDialog(null, "O arquivo não foi lido...", "Erro", JOptionPane.ERROR_MESSAGE);
            nome_arquivo_lido = "";
        }
    }//GEN-LAST:event_jButton2ActionPerformed

