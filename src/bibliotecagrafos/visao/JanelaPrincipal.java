    void imprimeNaAreaDeSaida(String str){
        txtSaida.setText(txtSaida.getText().concat(str));
    }
    
    private void validaSpinnerVertices(JSpinner spinner){
        int valor = (Integer)spinner.getValue(), qtd_vertices = grafo.getOrdem();
        if(qtd_vertices == 0){
            valor = 0;
        } else{
            if(valor <= 0){
                valor = 1;
            } else{
                if(valor > qtd_vertices){
                    valor = qtd_vertices;
                }
            }
        }
        spinner.setValue(valor);
    }
    
    private void habilitarOperacoes(){
        int qtd_vertices = grafo.getOrdem();
        btnGetOrdemGrafo.setEnabled(true);
        btnGetTamanhoGrafo.setEnabled(true);
        txtVerticeUnitario.setEnabled(qtd_vertices > 0);
        txtVerticeUnitario.setValue(1 % (qtd_vertices + 1));
        txtVerticeOrigemAresta.setEnabled(qtd_vertices > 0);
        txtVerticeOrigemAresta.setValue(1 % (qtd_vertices + 1));
        txtVerticeDestinoAresta.setEnabled(qtd_vertices > 0);
        txtVerticeDestinoAresta.setValue(1 % (qtd_vertices + 1));            
        
    }
    
