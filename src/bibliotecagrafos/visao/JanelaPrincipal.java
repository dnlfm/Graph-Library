    private void btnEscreverEmArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscreverEmArquivoActionPerformed
        if(JOptionPane.showConfirmDialog(null, "Você deseja escrever as informações da área de texto "
                + "Saída no arquivo:" + Grafo.QUEBRA_DE_LINHA + "\"" + txtArquivoSaida.getText() + "\"") == JOptionPane.YES_OPTION){
            String str = grafo.escreveResultadosEmArquivo(txtArquivoSaida.getText(), txtSaida.getText());
            if(str.isEmpty()){
                JOptionPane.showMessageDialog(null, "Não foi possível escrever no arquivo...", "Erro", JOptionPane.ERROR_MESSAGE);
            } else{
                JOptionPane.showMessageDialog(null, str);
            }
        }        
    }//GEN-LAST:event_btnEscreverEmArquivoActionPerformed

